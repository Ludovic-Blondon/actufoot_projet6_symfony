    /**
     * @return Query
     */
    public function findAllWithSearch(PostSearch $search): Query
    {
        $query = $this->createQueryBuilder('c');

        if ($search->getKeyWord()) {
            $query = $query
                ->Where('c.name LIKE :keyword')
                ->setParameter('keyword', '%' . $search->getKeyWord() . '%')
            ;
        }

        return $query->getQuery();
    }

